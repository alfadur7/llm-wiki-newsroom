def _check_adjacency_aliases(content: str, cluster_slugs: set[str], path: Path) -> list[str]:
    """Detect cluster-slug wikilinks missing a display-name alias in the
    `## Adjacent Domains & Scope` section."""
    m = ADJACENCY_SECTION_RE.search(content)
    if not m:
        return []
    issues: list[str] = []
    for target in UNALIASED_WIKILINK_RE.findall(m.group(1)):
        slug = target.strip()
        if slug in cluster_slugs:
            issues.append(
                f"  {path.name}: `## Adjacent Domains & Scope` has un-aliased cluster link "
                f"`[[{slug}]]` — use `[[{slug}|<cluster name>]]`"
            )
    return issues


def _fix_adjacency_aliases(path: Path, cluster_slug_to_name: dict[str, str]) -> list[str]:
    """Rewrite un-aliased `[[<cluster-slug>]]` inside the `## Adjacent Domains & Scope`
    section to `[[<slug>|<cluster name>]]`. The cluster name mapping is the
    authoritative `graph/_clusters.json` clusters[].name, so this fix is
    deterministic and side-effect free outside that section.

    Returns list of slugs that were rewritten.
    """
    content = path.read_text(encoding="utf-8")
    m = ADJACENCY_SECTION_RE.search(content)
    if not m:
        return []

    section = m.group(1)
    section_start = m.start(1)
    section_end = m.end(1)

    rewritten: list[str] = []

    def _replace(link_match: "re.Match[str]") -> str:
        slug = link_match.group(1).strip()
        if slug in cluster_slug_to_name:
            rewritten.append(slug)
            return f"[[{slug}|{cluster_slug_to_name[slug]}]]"
        return link_match.group(0)

    new_section = UNALIASED_WIKILINK_RE.sub(_replace, section)
    if rewritten:
        new_content = content[:section_start] + new_section + content[section_end:]
        atomic_write_text(path, new_content)
    return rewritten


def _check_cluster_name_drift(
    path: Path,
    content: str,
    fm: dict,
    slug: str,
    cluster_by_slug: dict,
    fix: bool,
) -> tuple[list[str], int, str]:
    """Compare frontmatter `title` + body H1 against `cluster["name"]` (SoT).

    The chain `cluster_labels.json` (human-edited) → `_clusters.json::
    clusters[].name` → overview MD frontmatter `title` + body `# H1` is
    one-way: when an editor renames a cluster (e.g. anchor refinement, slug
    relabel), the MD must follow. Same SoT is already used by skeleton
    creation and adjacency alias auto-fix in this module, so auto-correction
    in `--fix` mode is consistent.

    Skipped when slug ∉ cluster_by_slug (orphan MD — handled by the
    create/delete planner above).

    Returns (issues, fixed_count, new_content). `new_content` reflects any
    --fix rewrites; caller writes back if fixed_count > 0.
    """
    if slug not in cluster_by_slug:
        return [], 0, content
    expected_name = (cluster_by_slug[slug].get("name") or "").strip()
    if not expected_name:
        return [], 0, content

    issues: list[str] = []
    fixed = 0
    new_content = content

    fm_title = (fm.get("title") or "").strip()
    if fm_title and fm_title != expected_name:
        if fix:
            new_content = re.sub(
                r'^title:\s*.*$',
                lambda _m: f'title: "{expected_name}"',  # literal repl — name may hold \g/\1
                new_content,
                count=1,
                flags=re.MULTILINE,
            )
            fixed += 1
        else:
            issues.append(
                f"  {path.name}: frontmatter `title: \"{fm_title}\"` drift from "
                f"`_clusters.json::clusters[].name` `\"{expected_name}\"` — rerun --fix to sync"
            )

    h1_match = re.search(r"^# (.+?)\s*$", new_content, re.MULTILINE)
    if h1_match:
        h1_text = h1_match.group(1).strip()
        if h1_text != expected_name:
            if fix:
                new_content = re.sub(
                    r"^# .+?$",
                    lambda _m: f"# {expected_name}",  # literal repl — name may hold \g/\1
                    new_content,
                    count=1,
                    flags=re.MULTILINE,
                )
                fixed += 1
            else:
                issues.append(
                    f"  {path.name}: H1 `# {h1_text}` drift from cluster name "
                    f"`{expected_name}` — rerun --fix to sync"
                )

    return issues, fixed, new_content


def _split_sections(editor: str) -> dict[str, str]:
    """Split AUTO-stripped overview text into {`## <title>`: body} sections —
    preamble before the first H2 keyed `_preamble`; `###` subheaders stay in body."""
    sections: dict[str, str] = {}
    current = "_preamble"
    buffer: list[str] = []
    for line in editor.splitlines():
        m = re.match(r"^##\s+(.+?)\s*$", line)
        if m and not line.startswith("###"):
            if buffer:
                sections[current] = "\n".join(buffer)
            current = f"## {m.group(1).strip()}"
            buffer = []
        else:
            buffer.append(line)
    if buffer:
        sections[current] = "\n".join(buffer)
    return sections


def _link_metrics(content: str) -> dict:
    """Compute link-density metrics for Cluster Overview Evaluation Rubric."""
    editor = AUTO_BLOCK_RE.sub("", content)

    sections = _split_sections(editor)

    all_links = WIKILINK_RE.findall(editor)  # for X2 contradiction_refs (not yet moved)
    # enc.link-density(W1) measurement moved into the encyclopedia-writing skill — manifest params injected.
    total = enc_skill.count_wikilinks_in_editor_region(content, **_W1_PARAMS)

    by_section: dict[str, int] = {}
    for heading, body in sections.items():
        by_section[heading] = len(WIKILINK_RE.findall(body))

    lead_text = sections.get(LEAD_SECTION_HEADING, "")
    lead_links = len(WIKILINK_RE.findall(lead_text))
    lead_paras = _paragraph_count(lead_text) if lead_text else 1
    lead_density = lead_links / lead_paras if lead_paras else 0.0

    body_text = "\n".join(
        body for head, body in sections.items()
        if head not in (LEAD_SECTION_HEADING, "_preamble")
    )
    body_links = len(WIKILINK_RE.findall(body_text))
    body_paras = _paragraph_count(body_text) if body_text else 1
    body_density = body_links / body_paras if body_paras else 0.0

    lead_body_ratio = (lead_density / body_density) if body_density else float("inf")

    # enc.first-mention(W3) measurement moved into the encyclopedia-writing skill.
    # exempt_sections (`## Adjacent Domains & Scope` — a 2× adjacent-cluster citation
    # is normal)·strip_auto are manifest-injected.
    dup_total, duplicates = enc_skill.count_duplicate_links(content, **_W3_PARAMS)

    theme_stems: set[str] = set()
    if CONTRADICTIONS_DIR.exists():
        theme_stems = {
            p.stem for p in CONTRADICTIONS_DIR.glob("*.md")
            if not p.name.startswith("_")
        }
    contradiction_refs = 0
    for target in all_links:
        # Strip subdir prefix AND any `#anchor` (Xanadu citation anchoring) so
        # `[[contradictions/foo#section]]` resolves to the bare theme stem.
        stem = slug_only(target)
        if stem in theme_stems:
            contradiction_refs += 1

    # R1·L2·S6 are house-style (remain in overview.py). R2→con, B1·L1·L3 moved to the enc skill.
    r1_hot = _r1_hot_tokens(editor)
    r2_violations = con_skill.count_density_violations(editor, max_per_para=_R2_MAX_PARA)
    b1_hits = enc_skill.find_verdict_hits(editor)
    l1_violations = enc_skill.find_unaliased_slugs(editor, min_len=_L1_MIN_LEN)
    l2_violations = _l2_date_violations(editor)
    l3_violations = enc_skill.find_abbr_violations(editor)
    s6_long = _s6_long_sentences(editor)
    s6_para_anti = _s6_paragraph_no_period(editor)

    # G1·G2 — Phase 2 source schema meta-use (advisory, new dimension 7).
    # The body should differentiate a source's evidence grade (data/analysis/
    # forecast)·attribution strength·citation type. The Phase 2 schema attached
    # this to 1283 sources, so this auto-measures the gap where the cluster
    # overview EDITOR fails to leverage it.
    g1_grade_meta = cit_skill.count_grade_meta(editor)
    g2_cite_type_meta = cit_skill.count_cite_type_meta(editor)

    return {
        "total": total,
        "by_section": by_section,
        "lead_density": round(lead_density, 2),
        "body_density": round(body_density, 2),
        "lead_body_ratio": round(lead_body_ratio, 2) if lead_body_ratio != float("inf") else None,
        "duplicates": duplicates,
        "dup_total": dup_total,
        "contradiction_refs": contradiction_refs,
        "r1_hot": r1_hot,
        "r2_violations": r2_violations,
        "b1_hits": b1_hits,
        "l1_violations": l1_violations,
        "l2_violations": l2_violations,
        "l3_violations": l3_violations,
        "s6_long": s6_long,
        "s6_para_anti": s6_para_anti,
        "g1_grade_meta": g1_grade_meta,
        "g2_cite_type_meta": g2_cite_type_meta,
    }


def _editor_has_uncommitted(path: Path) -> bool:
    """True if working-tree EDITOR region differs from HEAD EDITOR region.

    Replaces a naive `git status --porcelain` check, which flags AUTO-only
    rebuilds as dirty and produced false Freshness warnings.
    """
    head = git_head_text(path)
    if head is None:
        return False
    try:
        current = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return False
    return _editor_hash(current) != _editor_hash(head)




def _freshness_line(path: Path, last_updated: object) -> str | None:
    """Flag frontmatter `last_updated` that trails the file's EDITOR
    edit state. AUTO block rebuilds do not trigger warnings.

    Advisory only — never gates exit code. Silent skip when git is
    unavailable or the field is missing/malformed.
    """
    if not isinstance(last_updated, str) or not re.match(r"^\d{4}-\d{2}-\d{2}$", last_updated):
        return None

    today = _date.today().isoformat()
    if _editor_has_uncommitted(path) and last_updated < today:
        return (
            f"    [Freshness] ⚠️ last_updated={last_updated} but EDITOR region "
            f"has uncommitted edits (today={today}) — update frontmatter before commit"
        )

    commit_date = _editor_last_commit_date(path)
    if commit_date and last_updated < commit_date:
        return (
            f"    [Freshness] ⚠️ last_updated={last_updated} but EDITOR last "
            f"changed in commit on {commit_date} — frontmatter was not refreshed"
        )

    return None


# ----- AUTO drift detector (Jaccard + source delta + top-10 shift) -----
# Advisory signal for operator: when AUTO blocks drift far from their
# committed baseline, EDITOR prose may reference stale facts/members.
# Thresholds tuned 2026-04-21 from ops-reliability retrospective — keep
# them centralized here so they're discoverable/tunable.

_WIKILINK_SLUG_RE = _LIB_WIKILINK_RE
_SOURCE_TOTAL_RE = re.compile(r"(\d+)\s+total\b")

DRIFT_JACCARD_STABLE = 0.85
DRIFT_JACCARD_REWRITE = 0.70
DRIFT_SOURCE_STABLE = 0.15
DRIFT_SOURCE_REWRITE = 0.30
DRIFT_TOP10_STABLE = 2
DRIFT_TOP10_REWRITE = 6


def _extract_auto_blocks(text: str) -> dict[str, str]:
    """Return {block_name: body} for each AUTO:X block in `text`."""
    return {m.group("name"): m.group("body") for m in AUTO_BLOCK_RE.finditer(text)}


def _auto_member_slugs(members_body: str) -> set[str]:
    """All [[slug]] targets in the AUTO:MEMBERS body (entities + concepts)."""
    return {m.group(1).strip() for m in _WIKILINK_SLUG_RE.finditer(members_body)}


def _auto_source_top(sources_body: str, n: int = 10) -> list[str]:
    """Top-N [[source-slug]] in AUTO:SOURCES body, in render order."""
    slugs: list[str] = []
    for m in _WIKILINK_SLUG_RE.finditer(sources_body):
        slugs.append(m.group(1).strip())
        if len(slugs) >= n:
            break
    return slugs


def _auto_source_total(sources_body: str) -> int:
    """Parse the 'N total' count in the AUTO:SOURCES body. 0 if absent."""
    m = _SOURCE_TOTAL_RE.search(sources_body)
    return int(m.group(1)) if m else 0


def _drift_line(path: Path, current_text: str) -> str | None:
    """Compute AUTO drift metrics vs git HEAD. Advisory only — 'stable'
    tier returns None so the lint line is silent for unchanged or near-
    stable clusters.

    Metrics:
      - member_jaccard: Jaccard similarity of AUTO:MEMBERS slug sets
      - source_delta:   |new_total − old_total| / max(old_total, 1)
      - top10_new:      count of AUTO:SOURCES top-10 slugs absent from old top-10

    Tiers (threshold constants at module top):
      - stable (all under threshold)   → None       (silent)
      - drift (one metric crosses mid) → 🟡 advisory (EDITOR review recommended)
      - rewrite (one crosses upper)    → 🔴 rewrite (--fix recommended)
    """
    head_text = git_head_text(path)
    if head_text is None:
        return None  # new file — no baseline
    old_blocks = _extract_auto_blocks(head_text)
    new_blocks = _extract_auto_blocks(current_text)
    if not (old_blocks.get("MEMBERS") or old_blocks.get("SOURCES")):
        return None  # baseline has no AUTO content

    old_m = _auto_member_slugs(old_blocks.get("MEMBERS", ""))
    new_m = _auto_member_slugs(new_blocks.get("MEMBERS", ""))
    if old_m or new_m:
        union = old_m | new_m
        jaccard = len(old_m & new_m) / len(union) if union else 1.0
    else:
        jaccard = 1.0

    old_n = _auto_source_total(old_blocks.get("SOURCES", ""))
    new_n = _auto_source_total(new_blocks.get("SOURCES", ""))
    delta = abs(new_n - old_n) / max(old_n, 1)

    old_top10 = set(_auto_source_top(old_blocks.get("SOURCES", ""), 10))
    new_top10 = _auto_source_top(new_blocks.get("SOURCES", ""), 10)
    top10_new = sum(1 for s in new_top10 if s not in old_top10)

    # Tier classification — any one metric crossing promotes the tier
    if jaccard < DRIFT_JACCARD_REWRITE or delta > DRIFT_SOURCE_REWRITE or top10_new >= DRIFT_TOP10_REWRITE:
        tier_icon = "🔴"
        tier_note = f"rewrite — `/wiki-lint overview {path.stem} --fix` recommended"
    elif jaccard < DRIFT_JACCARD_STABLE or delta > DRIFT_SOURCE_STABLE or top10_new > DRIFT_TOP10_STABLE:
        tier_icon = "🟡"
        tier_note = "drift — EDITOR review recommended"
    else:
        return None  # stable tier — silent

    sign = "+" if new_n >= old_n else "-"
    delta_str = f"{sign}{delta * 100:.0f}%"
    return (
        f"    [AUTO drift] member_jaccard={jaccard:.2f}  "
        f"source_delta={delta_str} (srcs {old_n}→{new_n})  "
        f"top10_new={top10_new}  {tier_icon} {tier_note}"
    )


def _load_anchor_to_cluster() -> dict[str, str]:
    """Build {entity-or-concept stem → owning cluster slug} from
    `graph/cluster_labels.json`. Anchor membership is the strongest
    'this hub belongs to that cluster's narrative core' signal — used
    by `_anchor_invasion_line()` to detect EDITOR narrative drift."""
    if not CLUSTER_LABELS_JSON.exists():
        return {}
    try:
        data = json.loads(CLUSTER_LABELS_JSON.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}

    out: dict[str, str] = {}
    # cluster_labels.json may use either {"labels": [...]} or be a bare list.
    labels = data.get("labels", data) if isinstance(data, dict) else data
    if not isinstance(labels, list):
        return {}
    for label in labels:
        slug = label.get("slug") if isinstance(label, dict) else None
        if not slug:
            continue
        for anchor in label.get("anchor_members", []):
            # anchor format: "entities/Foo.md" or "concepts/Bar.md"
            parts = anchor.split("/", 1)
            if len(parts) != 2:
                continue
            stem = parts[1].rsplit(".", 1)[0]
            out[stem] = slug
    return out


def _load_cluster_pairs() -> dict[str, set[str]]:
    """Build {cluster slug → set of paired cluster slugs} from optional
    `paired_with` field in `graph/cluster_labels.json`.

    paired_with declares cluster pairs whose anchor cross-references in
    overview EDITOR are intentional (e.g. data infra ↔ adoption,
    banking ↔ regulation) and should not raise [Anchor invasion] noise.
    Field is optional; clusters without it are treated as having no
    paired neighbors (any foreign anchor counts as invasion candidate).

    Pairs are normalized to be symmetric — declaring `A.paired_with: [B]`
    is enough; B's invasion check also exempts A's anchors. This means
    editors only need to write the pair once on whichever side is more
    natural."""
    if not CLUSTER_LABELS_JSON.exists():
        return {}
    try:
        data = json.loads(CLUSTER_LABELS_JSON.read_text(encoding="utf-8"))
    except (json.JSONDecodeError, OSError):
        return {}

    pairs: dict[str, set[str]] = {}
    labels = data.get("labels", data) if isinstance(data, dict) else data
    if not isinstance(labels, list):
        return {}
    for label in labels:
        if not isinstance(label, dict):
            continue
        slug = label.get("slug")
        peers = label.get("paired_with", [])
        if not slug or not isinstance(peers, list):
            continue
        for peer in peers:
            if not isinstance(peer, str) or peer == slug:
                continue
            pairs.setdefault(slug, set()).add(peer)
            # Symmetric: declaring once on either side is enough.
            pairs.setdefault(peer, set()).add(slug)
    return pairs


def _anchor_invasion_line(
    content: str,
    cluster_slug: str,
    anchor_to_cluster: dict[str, str],
    cluster_pairs: dict[str, set[str]] | None = None,
) -> str | None:
    """Detect EDITOR narrative invasion by other clusters' anchor_members.

    The case this catches: a cluster has been narrowed/split by re-clustering
    (e.g. 2026-04-25 telecom 73→19 split off AIDC·SKT·LGU+ to
    `aidc-infrastructure`), AUTO blocks were rebuilt and committed (so
    `[AUTO drift]` is silent against HEAD), but EDITOR text still describes
    the OLD broader cluster's core hubs. Looks for `[[entity]]`/`[[concept]]`
    references in EDITOR sections OTHER than `## Adjacent Domains & Scope`
    (which is the structural place for adjacent-cluster references) that
    are `anchor_member` of a DIFFERENT cluster.

    Two false-positive guards:

    1. **paired_with whitelist** (cluster_labels.json) — clusters whose
       narrative is genuinely paired/complementary (data infra ↔ adoption,
       banking ↔ regulation, etc.) cross-reference each other's anchors
       legitimately. Anchors owned by paired clusters are excluded from the
       invasion count, so paired pairs don't trigger drift signals.

    2. **max-owner concentration tiering** — total invasion count is noisy
       because a wide-scope cluster (e.g. bank-it-modernization narrative
       naturally spans payment·regulation·AI) accumulates many distributed
       sub-references. The honest stale signal is **a single foreign owner
       cluster contributing many anchors** (telecom: aidc-infrastructure 7/7
       = 100% — narrative drift; cloud-native: max owner 2/9 distributed —
       legitimate cross-context). Tier is based on max-owner-count, not
       total.

    Tiers (counts: max single owner cluster's anchor invasions, after
    paired exemption):
      - < INVASION_REVIEW_MIN  → silent (legitimate cross-context)
      - INVASION_REVIEW_MIN..  → 🟡 review advisory
      - INVASION_REWRITE_MIN.. → 🔴 rewrite advisory
    """
    if not anchor_to_cluster:
        return None

    paired = (cluster_pairs or {}).get(cluster_slug, set())

    editor = AUTO_BLOCK_RE.sub("", content)

    # Parse into sections, drop the structurally exempt section.
    sections = _split_sections(editor)

    EXEMPT = {"## Adjacent Domains & Scope"}
    narrative = "\n".join(body for head, body in sections.items() if head not in EXEMPT)

    # Group invasions by owning cluster, excluding paired peers.
    invasions: dict[str, list[str]] = {}
    paired_exempt: dict[str, list[str]] = {}
    seen: set[str] = set()
    for target in WIKILINK_RE.findall(narrative):
        # Strip subdir prefix AND any `#anchor` (Xanadu citation anchoring) so
        # an anchored hub ref still matches its anchor_member stem.
        stem = slug_only(target)
        if stem in seen:
            continue
        seen.add(stem)
        owner = anchor_to_cluster.get(stem)
        if not owner or owner == cluster_slug:
            continue
        if owner in paired:
            paired_exempt.setdefault(owner, []).append(stem)
        else:
            invasions.setdefault(owner, []).append(stem)

    if not invasions:
        return None

    # Tier on max-owner count (single concentrated foreign cluster is the
    # honest stale signal; distributed sub-refs across many clusters are
    # legitimate cross-context).
    sorted_owners = sorted(invasions.items(), key=lambda kv: -len(kv[1]))
    max_owner, max_items = sorted_owners[0]
    max_count = len(max_items)
    if max_count < INVASION_REVIEW_MIN:
        return None

    total = sum(len(v) for v in invasions.values())
    if max_count >= INVASION_REWRITE_MIN:
        tier_icon = "🔴"
        tier_note = f"narrative drift — `/wiki-lint overview {cluster_slug} --fix` recommended"
    else:
        tier_icon = "🟡"
        tier_note = "narrative drift — EDITOR review recommended"

    top = sorted_owners[:2]
    summary = "; ".join(f"{owner}({len(items)})" for owner, items in top)
    paired_note = ""
    if paired_exempt:
        paired_total = sum(len(v) for v in paired_exempt.values())
        paired_summary = ", ".join(f"{p}({len(items)})" for p, items in paired_exempt.items())
        paired_note = f"  [paired-exempt: {paired_total} refs — {paired_summary}]"
    return (
        f"    [Anchor invasion] {total} foreign anchor refs in narrative "
        f"(top: {summary}){paired_note}  {tier_icon} {tier_note}"
    )


def _readable_prose_chars(content: str) -> int:
    """EDITOR readable-prose character count (excluding whitespace·markdown symbols·wikilink markers).

    An approximation of Wikipedia:Article size's 'readable prose'. The
    frontmatter·AUTO blocks are excluded (auto-generated·meta, so not subject to
    the bloat verdict). Wikilinks keep only their display text.
    """
    text = strip_frontmatter(content)
    text = AUTO_BLOCK_RE.sub("", text)
    text = _WIKILINK_DISPLAY_RE.sub(r"\2", text)  # wikilink → display text
    text = re.sub(r"[#*\-|>`]", "", text)
    text = re.sub(r"\s+", "", text)
    return len(text)


_RECENT_SECTION_RE = re.compile(
    r"## Recent Changes\n(.*?)(?=\n## |\n<!-- AUTO|\Z)", re.DOTALL
)
_DATE_IN_TEXT_RE = re.compile(r"\b(\d{4})-(\d{1,2})(?:-(\d{1,2}))?\b")


def _recency_line(content: str, indent: str = "    ") -> str | None:
    """`## Recent Changes` recency advisory. 🟡 when the section's newest date exceeds RECENCY_STALE_DAYS.

    A timely section embedded in an evergreen page rots if left untended, so when
    the most recent dated item is older than the recommended window (≈3 months),
    an update is recommended. Silent when there is no date (pre-authoring stage).
    No effect on exit code.
    """
    m = _RECENT_SECTION_RE.search(content)
    if not m:
        return None
    dates: list[_date] = []
    for y, mo, d in _DATE_IN_TEXT_RE.findall(m.group(1)):
        try:
            dates.append(_date(int(y), int(mo), int(d) if d else 1))
        except ValueError:
            continue
    if not dates:
        return None
    newest = max(dates)
    age = (_date.today() - newest).days
    if age > RECENCY_STALE_DAYS:
        return (
            f"{indent}[Recency] '## Recent Changes' newest item {newest.isoformat()} "
            f"({age} days ago) 🟡 — no update in {RECENCY_STALE_DAYS}+ days, consider reflecting new events"
        )
    return None


def _size_line(content: str, indent: str = "    ") -> str | None:
    """One-line readable-prose bloat advisory. None (silent) when below threshold.

    The split direction is to spin detail out into hubs and keep the overview as
    summary + drill-down only (Wikipedia:Summary style). No effect on exit code —
    a change-magnitude monitoring layer.
    """
    n = _readable_prose_chars(content)
    if n >= SIZE_SPLIT_CHARS:
        return (
            f"{indent}[Size] readable_prose={n} chars (≥{SIZE_SPLIT_CHARS}) 🔴 split — "
            f"spin detail out into hubs and keep summary + drill-down only (Summary style)"
        )
    if n >= SIZE_REVIEW_CHARS:
        return (
            f"{indent}[Size] readable_prose={n} chars (≥{SIZE_REVIEW_CHARS}) 🟡 review — "
            f"watch the bloat; consider moving detail out to hubs"
        )
    return None


def _format_metrics_line(m: dict) -> list[str]:
    """Render Rubric metrics as advisory lines for lint output."""
    w1 = mark(m["total"] >= W1_MIN_LINKS)
    # An unmeasurable body (ratio=inf → serialized as None) is a PASS — the same
    # verdict as the L2-4 path (inf >= 1.0). An empty body itself is caught
    # separately by the W1·section checks.
    w2_ok = m["lead_body_ratio"] is None or m["lead_body_ratio"] >= 1.0
    w2 = mark(w2_ok)
    w3 = mark(m["dup_total"] <= _W3_THRESHOLD)
    x2 = mark(m["contradiction_refs"] > 0)
    r1 = mark(not m["r1_hot"])
    r2 = mark(len(m["r2_violations"]) <= _R2_VIOL_MAX)
    b1 = mark(len(m["b1_hits"]) <= _B1_VERDICT_MAX)
    l1 = mark(len(m["l1_violations"]) <= _L1_VIOL_MAX)
    l2 = mark(len(m["l2_violations"]) <= 3)
    l3 = mark(len(m["l3_violations"]) <= _L3_ABBR_MAX)
    s6 = mark(len(m["s6_long"]) <= 3)
    s6_pa = mark(len(m["s6_para_anti"]) == 0)
    # G1·G2 — Phase 2 source schema meta-use (advisory, dimension 7).
    g1 = mark(m["g1_grade_meta"] >= _G1_THRESHOLD)
    g2 = mark(m["g2_cite_type_meta"] >= _G2_THRESHOLD)

    ratio_str = f"{m['lead_body_ratio']:.2f}" if m["lead_body_ratio"] is not None else "n/a"
    lines = [
        f"    [Rubric] W1 links={m['total']} (≥{W1_MIN_LINKS}) {w1}  "
        f"W2 lead/body={m['lead_density']}/{m['body_density']} ratio={ratio_str} {w2}  "
        f"W3 dup={m['dup_total']} {w3}  "
        f"X2 contradiction_refs={m['contradiction_refs']} {x2}",
        f"    [Rubric] R1 hot={len(m['r1_hot'])} {r1}  "
        f"R2 dense_paras={len(m['r2_violations'])} {r2}  "
        f"B1 verdicts={len(m['b1_hits'])} {b1}  "
        f"L1 raw_slugs={len(m['l1_violations'])} {l1}",
        f"    [Rubric] L2 date_nonstd={len(m['l2_violations'])} {l2}  "
        f"L3 abbr_unexplained={len(m['l3_violations'])} {l3}  "
        f"S6 long_sents={len(m['s6_long'])} {s6}  "
        f"S6 para_anti={len(m['s6_para_anti'])} {s6_pa}",
        f"    [Rubric] G1 grade_meta={m['g1_grade_meta']} {g1}  "
        f"G2 cite_type_meta={m['g2_cite_type_meta']} {g2}",
    ]
    if m["duplicates"]:
        top = sorted(m["duplicates"], key=lambda x: -x[1])[:3]
        dup_str = ", ".join(f"{stem}×{n}" for stem, n in top)
        lines.append(f"    [Rubric] W3 top duplicates: {dup_str}")
    if m["r1_hot"]:
        top = m["r1_hot"][:3]
        tok_str = ", ".join(f"{t}×{n}" for t, n in top)
        lines.append(f"    [Rubric] R1 top hot tokens: {tok_str}")
    if m["l1_violations"]:
        top = list(dict.fromkeys(m["l1_violations"]))[:3]
        lines.append(f"    [Rubric] L1 raw slugs: {', '.join(top)}")
    if m["l3_violations"]:
        top = m["l3_violations"][:5]
        lines.append(f"    [Rubric] L3 unexplained: {', '.join(top)}")
    return lines


