def _build_cluster_map(clusters_data: dict) -> dict[str, dict]:
    seen: set[str] = set()
    out: dict[str, dict] = {}
    for c in clusters_data.get("clusters", []):
        slug = c["slug"]
        if slug in seen:
            continue
        seen.add(slug)
        out[slug] = c
    return out


def _md_overview_slugs() -> set[str]:
    if not OVERVIEWS_DIR.exists():
        return set()
    return {
        p.stem for p in OVERVIEWS_DIR.glob("*.md")
        if not p.name.startswith("_")
    }


def _plan_overview_changes(
    cluster_by_slug: dict[str, dict],
    only_slug: str | None,
) -> tuple[list[tuple[str, dict]], list[str]]:
    """Compute create+delete plan against `_clusters.json` SoT.

    creates: (slug, cluster_dict) tuples for cluster slugs that lack an MD.
    deletes: slugs whose MD exists but the slug is not in `_clusters.json`
             (orphan from a cluster rename, removed cluster, or ad-hoc MD).

    Pure planner — no filesystem mutation. Caller handles confirmation
    and execution via `_execute_overview_plan`.
    """
    json_slugs = set(cluster_by_slug.keys())
    md_slugs = _md_overview_slugs()

    if only_slug is not None:
        json_slugs &= {only_slug}
        md_slugs &= {only_slug}

    creates = [(slug, cluster_by_slug[slug]) for slug in sorted(json_slugs - md_slugs)]
    deletes = sorted(md_slugs - json_slugs)
    return creates, deletes


def _path_has_git_history(path: Path) -> bool:
    """True if `git log -- <path>` shows any prior commits.

    Detects the "cluster re-emergence" risk: an overview MD previously
    existed (authored via Cluster Overview Rubric), was deleted during a
    transient Leiden shift, and is now about to be overwritten with a
    blank skeleton — losing prior editorial content from the working tree
    until an operator thinks to run `git show <commit>:...`.

    Uses bytes capture + explicit UTF-8 decode with replacement fallback
    so Korean file names / commit messages do not crash on Windows
    cp949 default locale.
    """
    try:
        result = subprocess.run(
            ["git", "log", "--oneline", "--", str(path)],
            capture_output=True, timeout=5, check=False,
        )
        stdout = (result.stdout or b"").decode("utf-8", errors="replace")
        return bool(stdout.strip())
    except (subprocess.SubprocessError, FileNotFoundError, OSError):
        return False


def _warn_skeleton_overwrites_history(creates: list[tuple[str, dict]]) -> None:
    """Print a pre-prompt warning when skeleton creation would overwrite files
    with git history (i.e., cluster re-emergence after prior deletion).
    Called before `confirm_changes()` so operators see the risk upfront."""
    with_history = [
        slug for slug, _ in creates
        if _path_has_git_history(OVERVIEWS_DIR / f"{slug}.md")
    ]
    if not with_history:
        return
    print()
    print("⚠️  " + "=" * 70)
    print("⚠️  Found files with git history among the skeleton-creation targets")
    print("⚠️  " + "-" * 67)
    for slug in with_history:
        print(f"⚠️    - wiki/overviews/{slug}.md")
    print("⚠️  These files were authored in the past and then deleted when their cluster transiently vanished.")
    print("⚠️  Skeleton creation overwrites them with a blank template, and the existing edited content")
    print("⚠️  does not remain in the working tree (it is preserved in git history). To restore the edited content:")
    print("⚠️    git log -- wiki/overviews/<slug>.md        # check the previous commit")
    print("⚠️    git show <commit>:wiki/overviews/<slug>.md > wiki/overviews/<slug>.md")
    print("⚠️  restore it this way, then re-run --fix (a restored file is dropped from the create targets).")
    print("⚠️  " + "=" * 70)
    print()


def _execute_overview_plan(
    creates: list[tuple[str, dict]],
    deletes: list[str],
) -> int:
    """Apply a confirmed plan: create skeleton MDs + delete orphan MDs."""
    actions = 0
    if creates:
        OVERVIEWS_DIR.mkdir(parents=True, exist_ok=True)
    for slug, cluster in creates:
        path = safe_slug_path(OVERVIEWS_DIR, slug)
        atomic_write_text(path, _skeleton_overview(cluster))
        actions += 1
        print(f"  + created overviews/{slug}.md (skeleton from _clusters.json)")
    actually_deleted: list[str] = []
    for slug in deletes:
        path = safe_slug_path(OVERVIEWS_DIR, slug)
        if path.exists():
            path.unlink()
            actions += 1
            actually_deleted.append(slug)
            print(f"  - deleted overviews/{slug}.md (orphan MD — slug not in _clusters.json)")
    if actually_deleted:
        print_delete_cleanup_advisory(
            actually_deleted,
            kind="overview",
            check_cluster_labels=True,
        )
    return actions


def _check_overviews(clusters_data: dict, fix: bool, only_slug: str | None = None) -> tuple[list[str], int]:
    """Per-file schema + AUTO marker + Rubric + Freshness checks.

    File-existence reconciliation (create skeletons / delete orphans) is
    handled by the planner above and orchestrated in `run()`. This
    function only inspects MDs that already exist on disk after the plan
    has been applied.
    """
    issues: list[str] = []
    fixed_marker = 0
    fixed_cluster_slug = 0
    fixed_drift = 0
    alias_fixes = 0

    cluster_by_slug = _build_cluster_map(clusters_data)
    cluster_slug_to_name = {slug: c["name"] for slug, c in cluster_by_slug.items()}
    anchor_to_cluster = _load_anchor_to_cluster()
    cluster_pairs = _load_cluster_pairs()

    metrics_reports: list[str] = []
    for path in sorted(OVERVIEWS_DIR.glob("*.md")):
        if path.name.startswith("_"):
            continue
        if only_slug is not None and path.stem != only_slug:
            continue
        content = read_text_cached(path)
        fm = parse_frontmatter(content)
        issues.extend(check_frontmatter(fm, OVERVIEW_REQUIRED_FRONTMATTER, path))
        if fm.get("type") and fm["type"] != "overview":
            issues.append(f"  {path.name}: frontmatter `type` is `{fm['type']}` — expected `overview`")
        slug = path.stem
        if fm.get("cluster") and fm["cluster"] != slug:
            old_cluster = fm["cluster"]
            corrected = False
            if fix:
                new_content = re.sub(
                    rf'(?m)^cluster:\s*{re.escape(old_cluster)}\s*$',
                    f'cluster: {slug}',
                    content,
                    count=1,
                )
                if new_content != content:
                    content = new_content
                    atomic_write_text(path, content)
                    fm["cluster"] = slug
                    fixed_cluster_slug += 1
                    corrected = True
                    print(f"  ~ overviews/{path.name}: corrected frontmatter cluster `{old_cluster}` → `{slug}`")
            if not corrected:
                issues.append(f"  {path.name}: frontmatter `cluster: {old_cluster}` mismatches filename stem `{slug}`")
        drift_issues, drift_fixed, content = _check_cluster_name_drift(
            path, content, fm, slug, cluster_by_slug, fix
        )
        issues.extend(drift_issues)
        if drift_fixed:
            atomic_write_text(path, content)
            fixed_drift += drift_fixed
            print(f"  ~ overviews/{path.name}: synced cluster name drift ({drift_fixed} field{'s' if drift_fixed > 1 else ''})")
        issues.extend(check_sections(content, OVERVIEW_REQUIRED_SECTIONS, path))

        if fix:
            rewritten = _fix_adjacency_aliases(path, cluster_slug_to_name)
            if rewritten:
                alias_fixes += len(rewritten)
                print(f"  ~ overviews/{path.name}: aliased {', '.join(rewritten)}")
                content = read_text_cached(path)
        issues.extend(_check_adjacency_aliases(content, set(cluster_by_slug.keys()), path))

        missing_markers = check_auto_markers(content, OVERVIEW_AUTO_MARKERS, path)
        if missing_markers:
            if fix:
                new_content, added = migrate_markers(content, OVERVIEW_AUTO_MARKERS)
                if added:
                    atomic_write_text(path, new_content)
                    fixed_marker += 1
                    print(f"  ~ inserted AUTO markers into overviews/{path.name}: {', '.join(added)}")
            else:
                issues.extend(missing_markers)

        m = _link_metrics(content)
        file_lines: list[str] = []
        if m["total"] >= 20:
            file_lines.extend(_format_metrics_line(m))
        freshness = _freshness_line(path, fm.get("last_updated"))
        if freshness:
            file_lines.append(freshness)
        drift = _drift_line(path, content)
        if drift:
            file_lines.append(drift)
        invasion = _anchor_invasion_line(content, path.stem, anchor_to_cluster, cluster_pairs)
        if invasion:
            file_lines.append(invasion)
        size = _size_line(content)
        if size:
            file_lines.append(size)
        recency = _recency_line(content)
        if recency:
            file_lines.append(recency)
        if file_lines:
            metrics_reports.append(f"  overviews/{path.name}:")
            metrics_reports.extend(file_lines)

    if metrics_reports:
        print("\n[Cluster Overview Rubric metrics — advisory, see .claude/layers/overview.md Part 1]")
        for line in metrics_reports:
            print(line)

    total_fixed = fixed_marker + fixed_cluster_slug + fixed_drift + alias_fixes
    if fix and total_fixed:
        print(
            f"  overviews: {fixed_marker} marker-migrated, "
            f"{alias_fixes} alias-corrected, "
            f"{fixed_cluster_slug} cluster-slug-corrected, "
            f"{fixed_drift} drift-synced"
        )
    return issues, total_fixed


def _check_overview_md(cluster_slugs: set[str]) -> tuple[list[str], list[str]]:
    """Evaluate wiki/overview.md against the L2-4 Overview Evaluation Rubric."""
    issues: list[str] = []
    metrics_lines: list[str] = []

    if not OVERVIEW_MD_PATH.exists():
        issues.append("  wiki/overview.md missing")
        return issues, metrics_lines

    content = read_text_cached(OVERVIEW_MD_PATH)

    lead_buffer: list[str] = []
    body_sections: dict[str, str] = {}
    current_section: str | None = None
    section_buffer: list[str] = []
    for line in content.splitlines():
        if line.startswith("# ") and not line.startswith("## "):
            continue
        m = re.match(r"^##\s+(.+?)\s*$", line)
        if m and not line.startswith("###"):
            if current_section is not None:
                body_sections[current_section] = "\n".join(section_buffer)
            current_section = f"## {m.group(1).strip()}"
            section_buffer = []
        else:
            if current_section is None:
                lead_buffer.append(line)
            else:
                section_buffer.append(line)
    if current_section is not None:
        body_sections[current_section] = "\n".join(section_buffer)

    lead_text = "\n".join(lead_buffer)

    all_links = WIKILINK_RE.findall(content)

    # W1 — moved to the enc.link-density skill (L2-4: raw content, strip_auto=false manifest-injected).
    w1_count = enc_skill.count_wikilinks_in_editor_region(content, **_L24_W1_PARAMS)
    w1_ok = w1_count >= L24_W1_MIN_LINKS

    # W2
    lead_links = len(WIKILINK_RE.findall(lead_text))
    lead_paras = _paragraph_count(lead_text) if lead_text.strip() else 1
    lead_density = lead_links / lead_paras if lead_paras else 0.0
    body_text = "\n".join(body_sections.values())
    body_links = len(WIKILINK_RE.findall(body_text))
    body_paras = _paragraph_count(body_text) if body_text.strip() else 1
    body_density = body_links / body_paras if body_paras else 0.0
    w2_ratio = lead_density / body_density if body_density else float("inf")
    w2_ok = w2_ratio >= 1.0

    # W3 — moved to the enc.first-mention skill (L2-4 variant: per ### subsection·
    # drill-down excluded·lead excluded·raw content). All manifest params
    # (unit·exclude_patterns·include_preamble·strip_auto) are injected.
    w3_dup_total, _ = enc_skill.count_duplicate_links(content, **_L24_W3_PARAMS)
    w3_ok = w3_dup_total <= _L24_W3_THRESHOLD

    # D1(MECE cluster completeness) → consulting-writing. section_spans is reused by D2.
    _agg_con = getattr(con_skill, _AGG_CON_FN)(content, cluster_slugs=cluster_slugs)
    d1_count = _agg_con["d1_count"]
    d1_total = _agg_con["d1_total"]
    d1_ok = _agg_con["d1_ok"]
    d1_missing = _agg_con["d1_missing"]

    # D2(drill-down)·F1(Coatrack) → encyclopedia-writing. theme_stems is wiki-global.
    theme_stems: set[str] = set()
    if CONTRADICTIONS_DIR.exists():
        theme_stems = {
            pth.stem for pth in CONTRADICTIONS_DIR.glob('*.md')
            if not pth.name.startswith('_')
        }
    _agg_enc = getattr(enc_skill, _AGG_ENC_FN)(
        content, section_spans=_agg_con["section_spans"], all_links=all_links, theme_stems=theme_stems
    )
    d2_count = _agg_enc["d2_count"]
    d2_found = _agg_enc["d2_found"]
    d2_missing = _agg_enc["d2_missing"]
    d2_ok = len(d2_missing) == 0 and d2_found > 0
    f1_count = _agg_enc["f1_count"]
    f1_ok = f1_count == 0

    # D3(cross-cluster balance·due impartiality) → journalism-writing.
    _agg_jrn = getattr(jrn_skill, _AGG_JRN_FN)(all_links=all_links, cluster_slugs=cluster_slugs)
    max_refs = _agg_jrn["max_refs"]
    min_refs = _agg_jrn["min_refs"]
    d3_ratio = _agg_jrn["d3_ratio"]
    d3_ok = d3_ratio <= L24_D3_MAX_RATIO

    if not d1_ok:
        issues.append(
            f"  wiki/overview.md: D1 FAIL — only {d1_count}/{d1_total} clusters "
            f"as `## N. [[slug|...]]` sections (missing: {d1_missing})"
        )
    if not d2_ok and d2_found > 0:
        issues.append(
            f"  wiki/overview.md: D2 FAIL — clusters without self-slug "
            f"drill-down link inside their section: {d2_missing}"
        )
    if not f1_ok:
        issues.append(
            f"  wiki/overview.md: F1 FAIL — {f1_count} theme refs found "
            f"(L2-4 forbids [[<theme>]] links per Overview Taxonomy)"
        )

    # R1·L2·S6 are house-style (remain in overview.py). R2→con, B1·L1·L3 moved to the enc skill.
    # Exclude AUTO blocks (EDITOR region only) — same handling as cluster `_link_metrics`. This
    # keeps the marker tokens (AUTO·STATS·BEGIN·END)·raw [[slug]] in AUTO:STATS/SOURCES/MEMBERS
    # from being false-flagged as L3/L1. W1·W2·W3·D-series intentionally measure raw content (see above).
    editor = AUTO_BLOCK_RE.sub("", content)
    r1_hot = _r1_hot_tokens(editor)
    r2_violations = con_skill.count_density_violations(editor, max_per_para=_R2_MAX_PARA)
    b1_hits = enc_skill.find_verdict_hits(editor)
    l1_raw = enc_skill.find_unaliased_slugs(editor, min_len=_L1_MIN_LEN)
    l2_violations = _l2_date_violations(editor)
    l3_violations = enc_skill.find_abbr_violations(editor)
    s6_long = _s6_long_sentences(editor)
    s6_para_anti = _s6_paragraph_no_period(editor)

    r1_ok = len(r1_hot) == 0
    r2_ok = len(r2_violations) <= _R2_VIOL_MAX
    b1_ok = len(b1_hits) <= _B1_VERDICT_MAX
    l1_ok = len(l1_raw) <= _L1_VIOL_MAX
    l2_ok = len(l2_violations) <= 3
    l3_ok = len(l3_violations) <= _L3_ABBR_MAX
    s6_ok = len(s6_long) <= 3
    s6_para_ok = len(s6_para_anti) == 0

    if not l1_ok:
        issues.append(
            f"  wiki/overview.md: L1 FAIL — {len(l1_raw)} raw kebab-case slugs without "
            f"Slug alias: {', '.join(list(dict.fromkeys(l1_raw))[:5])}"
            f"{'...' if len(set(l1_raw)) > 5 else ''}"
        )

    ok = mark
    metrics_lines.append("wiki/overview.md:")
    metrics_lines.append(
        f"  [Rubric L2-4] W1 links={w1_count} (≥{L24_W1_MIN_LINKS}) {ok(w1_ok)}  "
        f"W2 lead/body={lead_density:.2f}/{body_density:.2f} ratio="
        f"{'inf' if w2_ratio == float('inf') else f'{w2_ratio:.2f}'} {ok(w2_ok)}  "
        f"W3 dup={w3_dup_total} {ok(w3_ok)}"
    )
    metrics_lines.append(
        f"  [Rubric L2-4] D1 clusters={d1_count}/{d1_total} {ok(d1_ok)}  "
        f"D2 drilldowns={d2_count}/{d2_found} {ok(d2_ok)}  "
        f"D3 balance={max_refs}/{min_refs}={d3_ratio:.2f} {ok(d3_ok)}  "
        f"F1 theme_refs={f1_count} {ok(f1_ok)}"
    )
    metrics_lines.append(
        f"  [Rubric L2-4] R1 hot={len(r1_hot)} {ok(r1_ok)}  "
        f"R2 dense_paras={len(r2_violations)} {ok(r2_ok)}  "
        f"B1 verdicts={len(b1_hits)} {ok(b1_ok)}  "
        f"L1 raw_slugs={len(l1_raw)} {ok(l1_ok)}"
    )
    metrics_lines.append(
        f"  [Rubric L2-4] L2 date_nonstd={len(l2_violations)} {ok(l2_ok)}  "
        f"L3 abbr_unexplained={len(l3_violations)} {ok(l3_ok)}  "
        f"S6 long_sents={len(s6_long)} {ok(s6_ok)}  "
        f"S6 para_anti={len(s6_para_anti)} {ok(s6_para_ok)}"
    )
    # G1·G2 — Phase 2 source schema meta-use (advisory, dimension 7 aggregate).
    # Measure only the EDITOR region outside the AUTO:STATS block — the statistics
    # lines in aggregate `wiki/overview.md` are auto-refreshed by build.py and lie
    # outside the EDIT writer's responsibility (`editor` reused).
    g1_grade_meta = cit_skill.count_grade_meta(editor)
    g2_cite_type_meta = cit_skill.count_cite_type_meta(editor)
    g1_ok = g1_grade_meta >= _L24_G1_THRESHOLD
    g2_ok = g2_cite_type_meta >= _L24_G2_THRESHOLD
    metrics_lines.append(
        f"  [Rubric L2-4] G1 grade_meta={g1_grade_meta} {ok(g1_ok)}  "
        f"G2 cite_type_meta={g2_cite_type_meta} {ok(g2_ok)}"
    )
    if r1_hot:
        top = r1_hot[:3]
        metrics_lines.append(
            f"  [Rubric L2-4] R1 top hot tokens: "
            + ", ".join(f"{t}×{n}" for t, n in top)
        )
    if l1_raw:
        top = list(dict.fromkeys(l1_raw))[:3]
        metrics_lines.append(
            f"  [Rubric L2-4] L1 raw slugs: " + ", ".join(top)
        )
    if l3_violations:
        top = l3_violations[:5]
        metrics_lines.append(
            f"  [Rubric L2-4] L3 unexplained: " + ", ".join(top)
        )

    size = _size_line(content, indent="  ")
    if size:
        metrics_lines.append(size)
    recency = _recency_line(content, indent="  ")
    if recency:
        metrics_lines.append(recency)

    return issues, metrics_lines


def run(target: str | None = None, fix: bool = False, auto_yes: bool = False) -> int:
    """Entry point for `python tools/lint.py overview [<target>] [--fix [--yes]]`.

    target:
      - None        → all overview files (L2-3 * 9 + L2-4)
      - "<slug>"    → single cluster overview
      - "aggregate" → wiki/overview.md only

    fix:
      - False → diagnostics only (no prompt, no mutations)
      - True  → planned reconciliation against `_clusters.json` SoT after
                confirmation:
                  create: cluster slug without MD → skeleton from cluster name
                  delete: MD without matching cluster slug → remove orphan
                Plus non-destructive content fixes on existing files
                (alias auto-correction, AUTO marker migration) — these
                proceed without extra prompt. When a specific cluster
                target is supplied, prints the Claude rewrite instruction
                block at the end.

    auto_yes:
      - False → create/delete plan triggers `confirm_changes()` prompt;
        non-TTY callers (Claude Code Bash, CI) abort the create/delete
        portion with a hint to re-invoke with --yes.
      - True  → skip the prompt and apply the plan unconditionally.
        Use only after reviewing the planned changes upstream.
    """
    clusters_data = _load_clusters()
    if clusters_data is None:
        return 2
    cluster_slugs: set[str] = {c["slug"] for c in clusters_data.get("clusters", [])}

    if target is None:
        scope = "all"
    elif target == "aggregate":
        scope = "aggregate"
    elif target in cluster_slugs:
        scope = "cluster"
    else:
        # Target may also be an MD-only slug (orphan). Allow it through so
        # the per-target run can offer to delete it.
        md_slugs = _md_overview_slugs()
        if target in md_slugs:
            scope = "cluster"
        else:
            print(
                f"ERROR: target '{target}' is not a valid cluster slug, "
                f"existing overview MD slug, or 'aggregate'.",
                file=sys.stderr,
            )
            print(f"Valid cluster slugs: {sorted(cluster_slugs)}", file=sys.stderr)
            print(f"Valid MD slugs: {sorted(md_slugs)}", file=sys.stderr)
            return 2

    ov_issues: list[str] = []
    ov24_issues: list[str] = []
    ov24_metrics: list[str] = []

    if scope in ("all", "cluster"):
        only_slug = target if scope == "cluster" else None
        cluster_by_slug = _build_cluster_map(clusters_data)
        creates, deletes = _plan_overview_changes(cluster_by_slug, only_slug)

        if fix:
            plan = {"create": [f"overviews/{s}.md" for s, _ in creates],
                    "delete": [f"overviews/{s}.md" for s in deletes]}
            _warn_skeleton_overwrites_history(creates)
            approved = confirm_changes(
                plan,
                context="overview MD ↔ _clusters.json sync",
                auto_yes=auto_yes,
            )
            if approved:
                _execute_overview_plan(creates, deletes)
            else:
                for slug, _c in creates:
                    ov_issues.append(
                        f"  overviews/{slug}.md missing (cluster `{slug}` in "
                        f"_clusters.json) — re-run --fix and confirm, or add --yes"
                    )
                for slug in deletes:
                    ov_issues.append(
                        f"  overviews/{slug}.md exists but slug `{slug}` is not in "
                        f"_clusters.json — re-run --fix and confirm to delete, or "
                        f"rebuild graph via `python tools/build.py clusters`"
                    )
        else:
            for slug, _c in creates:
                ov_issues.append(
                    f"  overviews/{slug}.md missing (cluster `{slug}` in _clusters.json) "
                    f"— `/wiki-lint overview --fix` creates skeleton (with confirmation)"
                )
            for slug in deletes:
                ov_issues.append(
                    f"  overviews/{slug}.md exists but slug `{slug}` is not in "
                    f"_clusters.json — `/wiki-lint overview --fix` deletes orphan "
                    f"(with confirmation), or rebuild graph via "
                    f"`python tools/build.py clusters`"
                )

        schema_issues, _actions = _check_overviews(clusters_data, fix, only_slug=only_slug)
        ov_issues.extend(schema_issues)

    if scope in ("all", "aggregate"):
        ov24_issues, ov24_metrics = _check_overview_md(cluster_slugs)
        if ov24_metrics:
            print("\n[L2-4 Overview Rubric metrics — advisory, see .claude/layers/overview.md Part 2]")
            for line in ov24_metrics:
                print(line)

    if fix and scope in ("cluster", "aggregate"):
        print()
        print("=" * 72)
        print("[/wiki-lint overview --fix] Claude rewrite instruction block")
        print("=" * 72)
        if scope == "cluster":
            print(f"Target: wiki/overviews/{target}.md (L2-3 cluster overview)")
            print()
            print("Execution order (Claude):")
            print("  1. Read .claude/layers/overview.md → Part 1 (Cluster Overview)")
            print("  2. Read .claude/layers/overview.md → Part 1 Rubric (Evaluation Rubric)")
            print(f"  3. Read wiki/overviews/{target}.md (current state)")
            print("  4. Authoring Guide Part 1 → perform execution steps 1–7")
            p1 = _roster_counts("overview-cluster")
            print(f"  5. Re-run `python tools/lint.py overview {target}` → check Rubric metrics·Freshness")
            print(f"  6. Iterate until {threshold_label(p1)} is achieved")
            print("  7. When done, update frontmatter `last_updated` to today's date")
        elif scope == "aggregate":
            cluster_count = sum(
                1 for p in OVERVIEWS_DIR.glob("*.md") if not p.name.startswith("_")
            )
            print("Target: wiki/overview.md (L2-4 root overview)")
            print()
            print("Execution order (Claude):")
            print("  1. Read .claude/layers/overview.md → Part 2 (Root Overview)")
            print("  2. Read .claude/layers/overview.md → Part 2 Rubric (Evaluation Rubric)")
            print(f"  3. Read wiki/overview.md (current state) + all {cluster_count} cluster overviews")
            print("  4. Authoring Guide Part 2 → perform execution steps 1–7")
            print("  5. Re-run `python tools/lint.py overview aggregate` → check Rubric L2-4 metrics")
            p2 = _roster_counts("overview-aggregate")
            print(f"  6. Iterate until {threshold_label(p2)} is achieved")

    all_issues = ov_issues + ov24_issues
    if all_issues:
        print(f"\n[overview scope: {len(all_issues)} schema issue(s)]")
        for i in all_issues:
            print(i)
        print(
            "\nFAIL — overview scope has schema deviations. "
            "See .claude/layers/overview.md for EDITOR authoring."
        )
        return 1

    print("\nOK — overview scope: schema + L2-4 required criteria")
    return 0


